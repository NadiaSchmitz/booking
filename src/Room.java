public class Room {

	private int room_no;
	private int room_place;
	private String status;
	
	public int getRoomNo() {
		return room_no;
	}
	
	public void setRoomNo(int room_no) {
		this.room_no = room_no;
	}
	
	public int getRoomPlace() {
		return room_place;
	}
	
	public void setRoomPlace(int room_place) {
		this.room_place = room_place;
	}
	
	public String getStatus() {
		return status;
	}
	
	public void setStatus(String status) {
		this.status = status;
	}
	
}
